function logout(){
				
	$.ajax({
		type: "POST",
		url: "ajax/logoutUser.php",
		success: function(result){ 
			window.location = "index.php";
		},
		error: function(errormessage) {
			alert('Error, tu sesión no pudo ser cerrada en estos momentos, por favor, intenta más tarde: ' + errormessage);
		}			 			 
	});
		
} //logout

